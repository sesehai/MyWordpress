function increment_views($doit = null, $id = 0) {
	global $wpdb, $views_options;
	if( $doit == 'in_process_postviews' ) {
		$post_id = $id;
	} elseif( isset($_GET['postviews_id']) ) {
		$post_id = intval($_GET['postviews_id']);
	} else {
		$post_id = 0;
	}
	$do_add = false;
	if( $post_id > 0 && ((defined('WP_CACHE') && WP_CACHE) || $doit == 'in_process_postviews') ) {
		$do_add = true;
		if( $views_options['check_reflash'] ) {
			$ip = preg_replace('/[^0-9a-fA-F:., ]/', '', $_SERVER['REMOTE_ADDR']);
			$wpdb->query('DELETE FROM ' . $wpdb->postviews_plus_reflash . ' WHERE look_time < DATE_SUB(CURRENT_TIMESTAMP, INTERVAL ' . $views_options['timeout'] . ' SECOND)');
			$data = $wpdb->get_var('SELECT look_time FROM ' . $wpdb->postviews_plus_reflash . ' WHERE post_id="' . $post_id . '" AND user_ip="' . $ip . '"');
			if( !$data ) {
				$wpdb->insert($wpdb->postviews_plus_reflash, array('post_id' => $post_id, 'user_ip' => $ip));
			} else {
				$do_add = false;
			}
		}
	}
	if( $do_add ) {
		$useragent = strtolower(trim($_SERVER['HTTP_USER_AGENT']));
		$bot = false;
		if( is_array($views_options['botagent']) ) {
			$regex = '/(' . str_replace('@@@@@@', ')|(', preg_quote(implode($views_options['botagent'], '@@@@@@'), '/')) . ')/si';
			$bot = preg_match($regex, $useragent);
		}
		if( $bot ) {
			$post_views = intval(get_post_meta($post_id, 'bot_views', true)) + 1;
			update_post_meta($post_id, 'bot_views', $post_views);
		} else {
			$post_views = intval(get_post_meta($post_id, 'views', true)) + 1;
			update_post_meta($post_id, 'views', $post_views);
		}
	}
	if( isset($_GET['count_id']) ) {
		change_views();
	}
}

