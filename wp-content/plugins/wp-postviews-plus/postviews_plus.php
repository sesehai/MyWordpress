function should_views_be_displayed() {
	global $views_options;
	$display_option = 0;
	if( is_home() ) {
		if( isset($views_options['display_home']) ) {
			$display_option = $views_options['display_home'];
		}
	} elseif( is_single() ) {
		if( isset($views_options['display_single']) ) {
			$display_option = $views_options['display_single'];
		}
	} elseif( is_page() ) {
		if( isset($views_options['display_page']) ) {
			$display_option = $views_options['display_page'];
		}
	} elseif( is_archive() ) {
		if( isset($views_options['display_archive']) ) {
			$display_option = $views_options['display_archive'];
		}
	} elseif( is_search() ) {
		if( isset($views_options['display_search']) ) {
			$display_option = $views_options['display_search'];
		}
	} else {
		if( isset($views_options['display_other']) ) {
			$display_option = $views_options['display_other'];
		}
	}
	return (($display_option == 0) || (($display_option == 1) && is_user_logged_in()));
}

function get_totalviews_stats_word($id, $with_bot, $type) {
	$add_word = $with_bot ? 1 : 0;
	switch( $type ) {
		case 'category':
			$add_word .= 1;
			break;
		case 'post_tag':
			$add_word .= 2;
			break;
		default:
			$add_word .= 0;
			break;
	}
	if( is_array($id) ) {
		$add_word .= implode('-', $id);
	} else {
		$add_word .= $id;
	}
	return $add_word;
}

function add_cache_stats($addin, $id, $with_bot = true, $type = '') {
	global $wpdb;
	static $first_run = true;
	$count_id = md5($_SERVER['REQUEST_URI']);
	if( $first_run ) {
		$wpdb->query('DELETE FROM ' . $wpdb->postviews_plus . ' WHERE count_id="' . $count_id . '"');
		$wpdb->query('DELETE FROM ' . $wpdb->postviews_plus . ' WHERE add_time<"' . (time() - 86400 * 7) . '"');
		$first_run = false;
	}
	$data = $wpdb->get_row('SELECT * FROM ' . $wpdb->postviews_plus . ' WHERE count_id="' . $count_id . '"');
	if( $data ) {
		switch( $addin ) {
			case 'tv':
				if( $data->tv == '' ) {
					$update = array('tv' => $id);
				} else {
					if( !in_array($id, explode(',', $data->tv)) ) {
						$update = array('tv' => $data->tv . ',' . $id);
					}
				}
				if( isset($update) ) {
					$wpdb->update($wpdb->postviews_plus, $update, array('count_id' => $count_id));
				}
				break;
			case 'gt':
				$add_word = get_totalviews_stats_word($id, $with_bot, $type);
				if( $data->gt == '' ) {
					$update = array('gt' => $add_word);
				} else {
					if( !in_array($add_word, explode(',', $data->gt)) ) {
						$update = array('gt' => $data->gt . ',' . $add_word);
					}
				}
				if( isset($update) ) {
					$wpdb->update($wpdb->postviews_plus, $update, array('count_id' => $count_id));
				}
				break;
		}
	} else {
		switch( $addin ) {
			case 'tv':
				$wpdb->insert($wpdb->postviews_plus, array('tv' => $id, 'count_id' => $count_id, 'add_time' => time()));
				break;
			case 'gt':
				$add_word = get_totalviews_stats_word($id, $with_bot, $type);
				$wpdb->insert($wpdb->postviews_plus, array('gt' => $add_word, 'count_id' => $count_id, 'add_time' => time()));
				break;
		}
	}
}

